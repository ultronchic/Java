public class Main {
    public static void main(String[] args) {
        // Создание и вывод точек
        Point point1 = new Point(3, 5);
        System.out.println(point1); // Вывод: {3;5}

        Point point2 = new Point(25, 6);
        System.out.println(point2); // Вывод: {25;6}

        Point point3 = new Point(7, 8);
        System.out.println(point3); // Вывод: {7;8}
    }
}